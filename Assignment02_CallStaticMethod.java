package assignmentsab53;
//Call Static Methods inside the main method
public class Assignment02_CallStaticMethod 
{
	static void add()
	{
		System.out.println("Addition");
	}
	static void sub()
	{
		System.out.println("Subtraction");
	}

	public static void main(String[] args) 
	{
      add();
      sub();
      
	}

}
